<?php
namespace mani\lottery\object;

/**
 * Class Error
 * @package mani\lottery\object
 */
class Error
{
    /**
     * @var string
     */
    private $message;

    /**
     * @param string $message
     */
    public function __construct($message)
    {
        $this->message = $message;
    }

    /**
     * @return string
     */
    public function getMessage()
    {
        return $this->message;
    }
}